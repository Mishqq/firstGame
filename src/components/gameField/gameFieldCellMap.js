function addCellToField(originObj, dx, idx){
	let obj = Object.assign({},
		originObj, {x: originObj.x+dx, c: originObj.c.map((originObj)=>{return originObj+3*idx})});
	clickAreas.push(obj)
}

// Добавляем все остальные ячейки для поля
